let _lang = null;

export function detect() {
  if (_lang) return _lang;
  const stored = localStorage.getItem('anvil_lang');
  if (stored && STRINGS[stored]) { _lang = stored; return _lang; }
  const nav = (navigator.language || 'en-US').substring(0, 2);
  if (nav === 'zh') _lang = 'zh-CN';
  else _lang = 'en-US';
  return _lang;
}

export function set(lang) {
  if (STRINGS[lang]) {
    _lang = lang;
    localStorage.setItem('anvil_lang', lang);
  }
}

export function t(key, params) {
  const lang = detect();
  let s = (STRINGS[lang] && STRINGS[lang][key]) || STRINGS['zh-CN'][key] || key;
  if (params) {
    for (const [k, v] of Object.entries(params)) {
      s = s.replace(`{${k}}`, v);
    }
  }
  return s;
}

export function supported() {
  return Object.keys(STRINGS);
}
